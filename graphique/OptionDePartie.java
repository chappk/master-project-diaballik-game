	private void validerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerButtonActionPerformed
		String j = "adversaire";
		int confirm = JOptionPane.showConfirmDialog(diabolik.getFenetrePrincipale(),
				"Voulez-vous quitter cette partie et donner la victoire au joueur " + j + "?",
				"Nouvelle partie",
				JOptionPane.YES_NO_OPTION);
		if (confirm == JOptionPane.YES_OPTION) {
			if (humainBlancBoolean && humainNoirBoolean) {
				diabolik.setModeDeJeu(0);
			} else if (humainBlancBoolean && !humainNoirBoolean) {
				diabolik.setModeDeJeu(1);
			} else if (!humainBlancBoolean && humainNoirBoolean) {
				diabolik.setModeDeJeu(3);
			} else {
				diabolik.setModeDeJeu(2);
			}

			if (humainNoirBoolean) {
				diabolik.setNiveauIA2(0);
			} else if (ordiFacileNoir) {
				diabolik.setNiveauIA2(1);
			} else if (ordiMoyenNoir) {
				diabolik.setNiveauIA2(2);
			} else {
				diabolik.setNiveauIA2(3);
			}

			if (humainBlancBoolean) {
				diabolik.setNiveauIA1(0);
			} else if (ordiFacileBlanc) {
				diabolik.setNiveauIA1(1);
			} else if (ordiMoyenBlanc) {
				diabolik.setNiveauIA1(2);
			} else {
				diabolik.setNiveauIA1(3);
			}

			if (basique) {
				diabolik.setBasique(true);
			} else {
				diabolik.setBasique(false);
			}

			diabolik.nouvellePartie();
			dispose();
		}
	}//GEN-LAST:event_validerButtonActionPerformed

