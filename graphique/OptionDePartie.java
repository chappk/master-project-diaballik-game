	public OptionDePartie(Diabolik diabolik, FenetrePrincipale parent) {
		super(parent, true);
		this.diabolik = diabolik;

		if (diabolik.getModeDeJeu() == Diabolik.OrdivsOrdi) {
			diabolik.shutdown();
		}

		initComponents();

		assignerValeurBlanc(diabolik.getNiveauIA1());
		assignerValeurNoir(diabolik.getNiveauIA2());

		if (diabolik.isBasique()) {
			basique = true;
			variante = false;
			basiqueSur.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphique/images/secondeVersion/fenetreConfiguration/selectionBasique.png")));
			varianteSur.setIcon(null);
		} else {
			basique = false;
			variante = true;
			basiqueSur.setIcon(null);
			varianteSur.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphique/images/secondeVersion/fenetreConfiguration/varianteSelection.png")));
		}



		setLocationRelativeTo(getParent());
		setVisible(true);
	}

	private void assignerValeurNoir(int lvl) {
		humainNoirBoolean = false;
		ordiDifficleNoir = false;
		ordiMoyenNoir = false;
		ordiFacileNoir = false;

		survolNoir.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphique/images/secondeVersion/fenetreConfiguration/sansSurvol.png")));

		switch (diabolik.getNiveauIA2()) {
			case 0:
				humainNoirBoolean = true;
				selectionNoir.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphique/images/secondeVersion/fenetreConfiguration/humainSelectionne.png")));
				break;
			case 1:
				ordiFacileNoir = true;
				selectionNoir.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphique/images/secondeVersion/fenetreConfiguration/facileSelectionne.png")));

				break;
			case 2:
				ordiMoyenNoir = true;
				selectionNoir.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphique/images/secondeVersion/fenetreConfiguration/moyenSelection.png")));

				break;
			case 3:
				ordiDifficleNoir = true;
				selectionNoir.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphique/images/secondeVersion/fenetreConfiguration/difficileSelectionne.png")));

				break;
		}
	}

